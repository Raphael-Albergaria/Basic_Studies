let x, y

x = parseFloat(prompt('Digite o primeiro número:'))
y = parseFloat(prompt('Digite o segundo número:'))

const Soma = x + y
const Subtração = x - y
const Divisão = x / y
const Multiplicação = x * y

alert(`Resultados:

Soma: ${Soma}
Subtração: ${Subtração}
Divisão: ${Divisão}
Multiplicação: ${Multiplicação}`)
